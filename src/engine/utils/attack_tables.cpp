U64 bishop_attack[SIZE][BISHOP_ATTACK_SIZE];
U64 rook_attack[SIZE][ROOK_ATTACK_SIZE];

//relevant bits
U8 bishop_relevant_bits[SIZE];
U8 rook_relevant_bits[SIZE];

//masks
U64 bishop_mask[SIZE];
U64 rook_mask[SIZE];

//magic
U64 rook_magic[SIZE];
U64 bishop_magic[SIZE];

U64 get_bishop_attack(U64 occ, int sq) {
   occ &= bishop_mask[sq];
   occ *= bishop_magic[sq];
   occ >>= 64-bishop_relevant_bits[sq];
   return bishop_attack[sq][occ];
}

U64 get_rook_attack(U64 occ, int sq) {
   occ &= rook_mask[sq];
   occ *= rook_magic[sq];
   occ >>= 64-rook_relevant_bits[sq];
   return rook_attack[sq][occ];
}
